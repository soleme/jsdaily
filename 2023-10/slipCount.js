const groupedData = {};

// 데이터를 costCenterName으로 그룹화
data.forEach(item => {
    const costCenterName = item.costCenterName;

    if (!groupedData[costCenterName]) {
        groupedData[costCenterName] = {
            groupName: costCenterName,
            members: [],
        };
    }

    groupedData[costCenterName].members.push(item);
});

// 그룹화된 데이터를 배열로 변환 및 인원 수와 amount 계산
